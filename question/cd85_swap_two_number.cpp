#include<bits/stdc++.h>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter two numbers: ";
    cin>>a>>b;
    cout<<"Before swapping: a="<<a<<" b="<<b<<endl;
    swap(a,b);
    cout<<"After swapping: a="<<a<<" b="<<b<<endl;
    return 0;
}
